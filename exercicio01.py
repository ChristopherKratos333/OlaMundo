'''
Elabore um progama que pergunte ao aluno suas 3 notas escolares.
O progama deverá calcular a média das 3 notas inseridas e
exibir esta média.
'''

nota1 = float(input("Informe suas 3 notas. 1º: "))
nota2 = float(input("2º: "))
nota3 = float(input("3º: "))
media = (nota1 + nota2 + nota3) / 3

print("Sua média aritmética é", media)




